    private void despLotesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_despLotesActionPerformed
        if(!despLotes.getSelectedItem().equals("Lote")){
            jLabel3.setVisible(true);
            jLabel4.setVisible(true);
            estadoActualLote.setVisible(true);
            estadoActualLote.setText(""+lotes.get(despLotes.getSelectedIndex()-1).getEstado());
            nuevoEstado.setVisible(true);
            actualizarEstado.setVisible(true);
        }else{
            jLabel3.setVisible(false);
            jLabel4.setVisible(false);
            estadoActualLote.setVisible(false);
            nuevoEstado.setVisible(false);
            actualizarEstado.setVisible(false);
        }
    }//GEN-LAST:event_despLotesActionPerformed

