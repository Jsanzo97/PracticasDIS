    public void mostrarLotes(ArrayList<Lote> lotes){
        this.lotes = lotes;
        for(int i = 0; i < lotes.size(); i++){
            despLotes.addItem("Id: "+lotes.get(i).getId()+" planta: "+lotes.get(i).getPlanta());
        }
        despLotes.setVisible(true);
    }
    
